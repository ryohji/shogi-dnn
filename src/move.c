struct move move_describe(unsigned code) {
    struct move result;
    div_t d = {code};
    d = div(d.quot, NUMBER_OF_ACTS);
    result.act = d.rem;
    d = div(d.quot, NUMBER_OF_ROWS);
    result.dan = d.rem;
    d = div(d.quot, NUMBER_OF_COLUMNS);
    result.suji = d.rem;
    d = div(d.quot, NUMBER_OF_KOMA);
    result.koma = d.rem;
    return result;
}

